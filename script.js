const contenidoCard = document.querySelector(".proyectos");
const modal = document.getElementById("modal");
const closeModal = document.querySelector(".close");

proyectos.forEach(proyecto => {
  const card = document.createElement("div");
  card.classList.add("card");
  card.style.backgroundImage = `url(${proyecto.imageUrl})`;

  const detalle = document.createElement("div");
  detalle.classList.add("detalle");
  detalle.innerHTML = `
        <p>${proyecto.medidas}</p>
        <h1>${proyecto.precio}</h1>
    `;

  card.appendChild(detalle);
  contenidoCard.appendChild(card);

  // Evento click para abrir modal
  card.addEventListener('click', () => {
    openModal(proyecto);
  });
});

// Función para abrir modal
function openModal(proyecto) {
  document.getElementById('modal-img').src = proyecto.imageUrl;
  document.getElementById('modal-desc').textContent = proyecto.caracteristicas;
  document.getElementById('modal-medidas').textContent = proyecto.medidas;
  document.getElementById('modal-precio').textContent = proyecto.precio;

  modal.style.display = "block";
  document.body.style.overflow = "hidden";
}

// Cerrar modal
closeModal.addEventListener('click', () => {
  modal.style.display = "none";
  document.body.style.overflow = "auto";
});

// Cerrar modal al hacer click fuera
modal.addEventListener('click', (e) => {
  if (e.target === modal) {
    modal.style.display = "none";
    document.body.style.overflow = "auto";
  }
});

// Cerrar modal con ESC
document.addEventListener('keydown', (e) => {
  if (e.key === 'Escape' && modal.style.display === "block") {
    modal.style.display = "none";
    document.body.style.overflow = "auto";
  }
});

document.querySelectorAll('.card').forEach(card => {
  card.addEventListener('mouseenter', () => {
    const randomColor = `hsl(${Math.floor(Math.random() * 360)}, 100%, 70%)`;
    card.style.boxShadow = `0 0 30px ${randomColor}, 0 0 60px ${randomColor}`;
    card.style.transform = 'translateY(-10px)';
  });

  card.addEventListener('mouseleave', () => {
    card.style.boxShadow = '0 4px 12px rgba(0,0,0,0.1)';
    card.style.transform = 'translateY(0)';
  });
});
